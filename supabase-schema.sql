-- 1. ENABLE REQUIRED EXTENSIONS
-- (uuid-ossp is usually enabled by default, but we ensure it)
extension if not exists "uuid-ossp";

-- 2. CREATE PROFILES TABLE
-- Stores public user profile data linked to auth.users
create table if not exists public.profiles (
  id uuid references auth.users on delete cascade primary key,
  username text,
  avatar_url text,
  updated_at timestamp with time zone default timezone('utc'::text, now())
);

comment on table public.profiles is 'Public profile data for each user.';

-- 3. CREATE GUESTBOOK TABLE
-- Stores anonymous/public guestbook entries
create table if not exists public.guestbook (
  id bigint generated by default as identity primary key,
  name text not null default 'Anonymous',
  message text not null,
  created_at timestamp with time zone default timezone('utc'::text, now())
);

comment on table public.guestbook is 'Guestbook entries left by visitors.';

-- 4. CREATE COMMENTS TABLE
-- Stores nested chapter comments
create table if not exists public.comments (
  id uuid default uuid_generate_v4() primary key,
  user_id uuid references auth.users on delete cascade not null,
  volume_id text not null,
  chapter_title text not null,
  content text not null,
  parent_id uuid references public.comments(id) on delete cascade,
  created_at timestamp with time zone default timezone('utc'::text, now())
);

comment on table public.comments is 'Nested comments on book chapters.';

-- 5. CREATE COMMENT_REACTIONS TABLE
-- Stores upvotes/downvotes on comments
create table if not exists public.comment_reactions (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users on delete cascade not null,
  comment_id uuid references public.comments(id) on delete cascade not null,
  reaction_type text not null check (reaction_type in ('upvote', 'downvote')),
  created_at timestamp with time zone default timezone('utc'::text, now()),
  unique (user_id, comment_id)
);

comment on table public.comment_reactions is 'Upvotes and downvotes on comments.';

-- 6. CREATE READING_PROGRESS TABLE
-- Stores user's last read position per volume
create table if not exists public.reading_progress (
  user_id uuid references auth.users on delete cascade not null,
  volume_id text not null,
  chapter_index integer not null default 0,
  scroll_percentage integer not null default 0,
  last_read timestamp with time zone default timezone('utc'::text, now()),
  percentage integer not null default 0,
  primary key (user_id, volume_id)
);

comment on table public.reading_progress is 'Tracks user reading progress per volume.';

