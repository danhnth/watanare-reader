-- ============================================================
-- CREATE DELETE_USER RPC FUNCTION
-- Called when a user deletes their account via ProfileModal
-- ============================================================

create or replace function public.delete_user()
returns void
language plpgsql
security definer
set search_path = public
as $$
begin
  -- Delete user's reading progress
  delete from public.reading_progress where user_id = auth.uid();
  
  -- Delete user's comment reactions
  delete from public.comment_reactions where user_id = auth.uid();
  
  -- Delete user's comments
  delete from public.comments where user_id = auth.uid();
  
  -- Delete user's profile
  delete from public.profiles where id = auth.uid();
  
  -- Delete the auth user (this requires service role or admin)
  -- In standard Supabase, users cannot delete themselves via RLS.
  -- We call this via supabase.auth.admin.deleteUser() on server,
  -- or we rely on the app calling auth admin API.
  -- For client-side deletion, we handle it differently.
  -- 
  -- NOTE: The current app calls supabase.rpc('delete_user').
  -- If the user is not an admin, the auth.users deletion will fail.
  -- You should handle account deletion via a Supabase Edge Function
  -- or manually from the Supabase Dashboard.
  --
  -- Uncomment below ONLY if running as service role / in Edge Function:
  -- delete from auth.users where id = auth.uid();
end;
$$;

-- Allow authenticated users to execute this function
create policy "Authenticated users can execute delete_user"
  on public.profiles
  for all
  using (auth.uid() = id);

-- ============================================================
-- CREATE HANDLE_NEW_USER TRIGGER
-- Automatically creates a profile row when a new user signs up
-- via email/password or OAuth. This ensures OAuth users also have profiles.
-- ============================================================

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.profiles (id, username, avatar_url, updated_at)
  values (
    new.id,
    coalesce(
      new.raw_user_meta_data->>'username',
      new.raw_user_meta_data->>'full_name',
      new.raw_user_meta_data->>'name',
      split_part(new.email, '@', 1)
    ),
    new.raw_user_meta_data->>'avatar_url',
    timezone('utc'::text, now())
  );
  return new;
end;
$$;

-- Drop trigger if exists so script can be re-run
drop trigger if exists on_auth_user_created on auth.users;

create trigger on_auth_user_created
  after insert on auth.users
  for each row execute function public.handle_new_user();

